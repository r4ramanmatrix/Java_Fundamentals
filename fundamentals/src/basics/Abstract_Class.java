package basics;

public abstract class Abstract_Class {
	
	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	public void setAge(int age) {
		this.age=age;
	}
	
	abstract public String DisplayName();
	abstract public int DisplayAge();

}
